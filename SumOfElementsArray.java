class SumOfElementsArray {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
        int i;
        int sum = 0;

        int[] arr = { 1, 2, 3, 4, 5 };
        for (i = 0; i < 5; i++) {
            // if (arr[i] = arr[]) {
            // System.out.println("yes");
            sum = arr[i] + sum;
        }
        System.out.println(sum);
    }

}
